package Demo1;

public class type {
    public static void main(String[] args) {
        // 整型: 整数类型
        int aa = 1000000000;
        System.out.println(aa);
        // 长整型:大整数类型
        long bb = 3000000000L;
        System.out.println(bb);

        byte cc = 127;
        short dd = 2222;

        // 小数
        double ee = 1.11;
        float ff = 2.22F;
    }
}
