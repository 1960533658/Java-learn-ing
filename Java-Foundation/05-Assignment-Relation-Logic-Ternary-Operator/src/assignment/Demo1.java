package assignment;

public class Demo1 {
    public static void main(String[] args) {
        // 用来保存那个变量、常量保存数据的运算符
        int aa = 2;
        System.out.println(aa+=2); // 4
        System.out.println(aa-=2); // 2
        System.out.println(aa*=2); // 4
        System.out.println(aa/=2); // 2
        System.out.println(aa%=2); // 0
    }
}
